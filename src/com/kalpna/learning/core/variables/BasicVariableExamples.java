package com.kalpna.learning.core.variables;

public class BasicVariableExamples {
    public static void main(String[] args)
    {
        int age = 20;
        short population = -32768; //-32768-3276
        byte count = -55; // -127 to 128
        long distance = 12345678912344L;
        // floating data types
        float pi = 3.14f;
        double temperature = 25.999;
        char grade = 'A';
        boolean isStudent = true;
        System.out.println("Grade:: "+ grade);

    }}
